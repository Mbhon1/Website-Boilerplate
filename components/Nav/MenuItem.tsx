import { motion } from "framer-motion";
import Link from "next/link";

const variants = {
  open: {
    y: 0,
    opacity: 1,
    transition: {
      y: { stiffness: 1000, velocity: -100 },
    },
  },
  closed: {
    y: 50,
    opacity: 0,
    transition: {
      y: { stiffness: 1000 },
    },
  },
};

type IndexProps = {
  i: string;
  link: string;
};

export const MenuItem = ({ i, link }: IndexProps) => {
  return (
    <motion.li
      variants={variants}
      whileHover={{ scale: 1.1 }}
      whileTap={{ scale: 0.95 }}
    >
      <Link href={link}>
        <div className="text-placeholder text-2xl font-extrabold">{i}</div>
      </Link>
    </motion.li>
  );
};
